    private void setupTable() {
        // Colonnes correspondant au controller: ID, Date/Heure, Patient, Telephone, Categorie, Prix
        String[] colonnes = {"ID", "Date/Heure", "Patient", "Telephone", "Categorie", "Prix (DH)"};
        tableModel = new DefaultTableModel(colonnes, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tableConsultationsNonPayees.setModel(tableModel);
        tableConsultationsNonPayees.setRowHeight(25);
        tableConsultationsNonPayees.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
    }
    
    private void refreshTable() {
        Object[][] data = consultationController.getConsultationsNonPayeesForTable();
        tableModel.setRowCount(0);
        for (Object[] row : data) {
            tableModel.addRow(row);
        }
        updateTotal();
    }
    
    private void updateTotal() {
        double total = 0;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            // Le prix est dans la colonne 5 (index 5)
            String prixStr = (String) tableModel.getValueAt(i, 5);
            try {
                double prix = Double.parseDouble(prixStr.replace(" DH", "").replace(",", ".").trim());
                total += prix;
            } catch (NumberFormatException e) {
                // Ignorer
            }
        }
        lblTotal.setText("Total impaye : " + String.format("%.2f", total) + " DH");
    }

