    private void btnValiderPaiementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValiderPaiementActionPerformed
        int selectedRow = tableConsultationsNonPayees.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Veuillez selectionner une consultation", "Attention", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
            "Confirmer le paiement de cette consultation ?",
            "Confirmation",
            javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            int consultationId = (int) tableModel.getValueAt(selectedRow, 0);
            String result = consultationController.validerPaiement(consultationId);
            
            if (result.contains("valid") || result.contains("succes")) {
                javax.swing.JOptionPane.showMessageDialog(this, result, "Succes", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                refreshTable();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, result, "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnValiderPaiementActionPerformed

