    private void setupTable() {
        String[] colonnes = {"ID", "Nom", "Téléphone", "Email"};
        tableModel = new DefaultTableModel(colonnes, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tablePatients.setModel(tableModel);
        tablePatients.setRowHeight(25);
        tablePatients.setSelectionMode(javax.swing.ListSelectionModel.SINGLE_SELECTION);
    }
    
    private void setupActions() {
        // Clic sur une ligne -> remplir le formulaire
        tablePatients.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int selectedRow = tablePatients.getSelectedRow();
                if (selectedRow >= 0) {
                    txtNomPatient.setText((String) tableModel.getValueAt(selectedRow, 1));
                    txtTelPatient.setText((String) tableModel.getValueAt(selectedRow, 2));
                    txtEmailPatient.setText((String) tableModel.getValueAt(selectedRow, 3));
                }
            }
        });
    }
    
    private void refreshTable() {
        Object[][] data = patientController.getPatientsForTable();
        tableModel.setRowCount(0);
        for (Object[] row : data) {
            tableModel.addRow(row);
        }
    }
    
    private void clearForm() {
        txtNomPatient.setText("");
        txtTelPatient.setText("");
        txtEmailPatient.setText("");
        tablePatients.clearSelection();
    }

