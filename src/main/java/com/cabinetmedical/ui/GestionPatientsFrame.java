    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        String result = patientController.createPatient(
            txtNomPatient.getText(),
            txtTelPatient.getText(),
            txtEmailPatient.getText()
        );
        
        if (result.contains("succès")) {
            javax.swing.JOptionPane.showMessageDialog(this, result, "Succès", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            clearForm();
            refreshTable();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, result, "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        int selectedRow = tablePatients.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Veuillez sélectionner un patient", "Attention", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int id = (int) tableModel.getValueAt(selectedRow, 0);
        String result = patientController.updatePatient(
            id,
            txtNomPatient.getText(),
            txtTelPatient.getText(),
            txtEmailPatient.getText()
        );
        
        if (result.contains("succès")) {
            javax.swing.JOptionPane.showMessageDialog(this, result, "Succès", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            clearForm();
            refreshTable();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, result, "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModifierActionPerformed

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        int selectedRow = tablePatients.getSelectedRow();
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Veuillez sélectionner un patient", "Attention", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Êtes-vous sûr de vouloir supprimer ce patient ?", 
            "Confirmation", 
            javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            int id = (int) tableModel.getValueAt(selectedRow, 0);
            String result = patientController.deletePatient(id);
            
            if (result.contains("succès")) {
                javax.swing.JOptionPane.showMessageDialog(this, result, "Succès", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                clearForm();
                refreshTable();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, result, "Erreur", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSupprimerActionPerformed

    private void btnRechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechercherActionPerformed
        String criteria = txtRecherche.getText().trim();
        if (criteria.isEmpty()) {
            refreshTable();
        } else {
            Object[][] data = patientController.searchPatientsForTable(criteria);
            tableModel.setRowCount(0);
            for (Object[] row : data) {
                tableModel.addRow(row);
            }
        }
    }//GEN-LAST:event_btnRechercherActionPerformed

    private void btnActualiserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualiserActionPerformed
        txtRecherche.setText("");
        refreshTable();
    }//GEN-LAST:event_btnActualiserActionPerformed

